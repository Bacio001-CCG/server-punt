const cleanMap = [...new Map(REPLACEMENTS).entries()].sort((a, b) => b[0].length - a[0].length);
const SKIP_KEYS = new Set(["type", "id", "icon", "url"]);

function fixText(text) {
  if (!text || /^https?:\/\//.test(text) || /^info@/.test(text)) return text;
  let out = text;
  for (const [from, to] of cleanMap) out = out.split(from).join(to);
  return out;
}

function walk(value, key) {
  if (typeof value === "string") {
    if (SKIP_KEYS.has(key)) return value;
    return fixText(value);
  }
  if (Array.isArray(value)) return value.map((v) => walk(v, key));
  if (value && typeof value === "object") {
    const out = {};
    for (const [k, v] of Object.entries(value)) out[k] = walk(v, k);
    return out;
  }
  return value;
}

